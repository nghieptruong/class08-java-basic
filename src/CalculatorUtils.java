public class CalculatorUtils {

    //Cong 2 so nguyen
    public static int sumNumbers(int x, int y) {
        int result = x + y;
        return result;
    }

    public static float sumNumbers(float x, float y) {
        float result = x + y;
        return result;
    }

    public static int sumNumbers(int x, int y, int z) {
        int result = x + y + z;
        return result;
    }

    public static double sumNumbers(int x, double y) {
        double result = x + y;
        return result;
    }

    public static double sumNumbers(double y, int x) {
        double result = x + y;
        return result;
    }

    //overloading:
    //1. bat buoc phai cung ten
    //2. khac tham so:
    //  2.1. kieu du lieu tham so
    //  2.2. khac so luong tham so
    //  2.3. khac vi tri tham so

}
